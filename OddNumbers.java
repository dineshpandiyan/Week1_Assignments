package week1.program.assignments.completed;

public class OddNumbers {

	public static void main(String[] args) {
		// ODD Numbers 1 to 10;
		
		for(int i=1; i<=10; i++) {
			
			if(i%2 == 1) {
				
				System.out.println("The ODD Numbers are = " + i);
				
			}
			
			
		}

	}

}
